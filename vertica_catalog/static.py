def varchar_ddl(d):
    basename = TYPE_OIDS[int(d['typeoid'])][0]
    if d.get('typemod', '-1') != '-1':
        return '%s(%d)' % (basename, int(d['typelen']))
    return basename


def numeric_ddl(d):
    if d.get('typemod', '-1') != '-1':
        return 'numeric(%d,%d)' % ((int(d['typemod']) - 4) >> 16, (int(d['typemod']) - 4) & 1023)
    return 'numeric'


def timestamp_ddl(d):
    if d.get('typemod', '-1') != '-1':
        return 'timestamp(%d)' % int(d['typemod'])
    return 'timestamp'


def interval_ddl(d):
    if d.get('typemod', '-1') != '-1':
        return 'interval(%d)' % int(d['typemod'])
    return 'interval'


